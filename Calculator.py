number1= int(input("Enter Your 1st Number: "))
action = input("Enter Your Action: ")
number2 = int(input("Enter Your 2nd Number: "))

if action == "+":
  print(number1 + number2)
elif action == "-":
  print(number1 - number2)
elif action == "*":
  print(number1 * number2)
elif action == "/":
  print(number1 / number2)

